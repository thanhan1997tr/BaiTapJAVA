    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (txtSoXe.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Chọn số xe để sửa");
        }
        else{
            try {
                String soxe = txtSoXe.getText();
                String tinhtrang = "";
                if(rbXeCu.isSelected()){
                    tinhtrang = "0";
                }
                else
                    tinhtrang = "1";
                //String maloai = cbbTenLoai.getSelectedItem().toString();
                String maloai = String.valueOf(xeBll.getValueComboBoxTenLoai(cbbTenLoai));
                XeDTO xe = new XeDTO(soxe, tinhtrang, maloai);
                if (xeBll.SuaXe(xe)){
                    xeBll.LoadDSXe(tbXe);
                    Clear_ALL();
                    JOptionPane.showMessageDialog(null, "Sửa thành công.");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Sửa không thành công.");
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

