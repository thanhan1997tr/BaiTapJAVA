    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (txtSoXe.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập số xe.");
        }
        else{
            try {
                if(xeBll.getTrungMa(txtSoXe.getText())){
                    JOptionPane.showMessageDialog(null, "Số xe bị trùng.");
                }
                else{
                    String soxe = txtSoXe.getText();
                    String tinhtrang = "";
                    if(rbXeCu.isSelected()){
                        tinhtrang = "0";
                    }
                    else
                        tinhtrang = "1";
                    //String maloai = cbbTenLoai.getSelectedItem().toString();
                    String maloai = String.valueOf(xeBll.getValueComboBoxTenLoai(cbbTenLoai));
                    XeDTO xe = new XeDTO(soxe, tinhtrang, maloai);
                    if (xeBll.ThemXe(xe)){
                        xeBll.LoadDSXe(tbXe);
                        Clear_ALL();
                        JOptionPane.showMessageDialog(null, "Thêm thành công.");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Thêm không thành công.");
                    }
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int iDongDaChon = tbXe.getSelectedRow();
        if (iDongDaChon == -1){
            JOptionPane.showMessageDialog(null, "Mời chọn 1 khách hàng.");
        }
        else{
            try {
                if(JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa xe: \"" + txtSoXe.getText().toUpperCase() + "\" không?", "Thông Báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    try {
                        if(xeBll.XoaXe(txtSoXe.getText())){
                            xeBll.LoadDSXe(tbXe);
                            Clear_ALL();
                            JOptionPane.showMessageDialog(null, "Xóa thành công.");
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Xóa không thành công.");
                        }
                    } catch (Exception e) {
                    }
                }
            } catch (Exception e) {
            }
        }
        //lb.setText(String.valueOf(xeBll.getComboboxValue(cbbTenLoai)));
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbXeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbXeMouseClicked
        // TODO add your handling code here:
        int iDongDaChon = tbXe.getSelectedRow();
        TableModel model = tbXe.getModel();
        
        txtSoXe.setText(model.getValueAt(iDongDaChon, 0).toString());
        if(model.getValueAt(iDongDaChon, 1) == "Xe cũ"){
            rbXeCu.setSelected(true);
        }
        else
            rbXeMoi.setSelected(true);
        cbbTenLoai.setSelectedItem(model.getValueAt(iDongDaChon, 2).toString());
    }//GEN-LAST:event_tbXeMouseClicked

